fn day1(value: &str) -> String {
    let mut firstDigit: char = ' ';
    let mut secondDigit: char = ' ';

    for c in value.chars() {
        if c.is_digit(10) {
            if firstDigit.is_whitespace() {
                firstDigit = c;
                secondDigit = c;
            } else {
                secondDigit = c;
            }
        }
    }

    return format!("{}{}", firstDigit, secondDigit);
}

fn calibrate(values: &Vec<String>) -> u32 {
    let mut sum: u32 = 0;

    for s in values {
        let result: u32 = day1(s).parse().unwrap();
        sum = sum + result;
    }

    sum
}

#[test]
fn getNumbers() {
    let result = day1("1abc2");
    assert_eq!(result, "12");

    let result = day1("pqr3stu8vwx");
    assert_eq!(result, "38");

    let result = day1("a1b2c3d4e5f");
    assert_eq!(result, "15");

    let result = day1("treb7uchet");
    assert_eq!(result, "77");
}

